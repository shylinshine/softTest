package org.example;

public class DrugArea {

    public static double circleArea(double r) {
        return 3.14 * r * r;
    }
    public static double rectangleArea(double chang, double kuan) {
        return chang * kuan;
    }
    public static double sumArea(double area1,double area2) {
        return  area1 + area2;
    }


}
